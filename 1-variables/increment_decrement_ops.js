// Increase the value of variable b by 1 and decrease of c by 1
let b = 0;      // b contains 0
b += 1;         // b contains 1
b++;            // b contains 2
console.log(b); // Shows 2

let c = 10;      // b contains 10
c -= 1;         // b contains 9
c--;            // b contains 8
console.log(c); // Shows 8
